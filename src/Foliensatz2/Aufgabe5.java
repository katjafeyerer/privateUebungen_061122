package Foliensatz2;

public class Aufgabe5 {
    public static void main(String[] args) {
        int i =0;

        while(i<= 10) {
            i++;
            if(i%2 ==0) {
                System.out.println(i);
            }

        }

        int j = 10;

        while(j>0) {
            j--;
            if(j%2==0) {
                System.out.println(j);
            }
        }

        for (int k = 0; k <= 10 ; k++) {
            if(k%2 ==0) {
                System.out.println(k);
            }

        }
        for (int k = 10; k >= 0 ; k--) {
            if(k%2 ==0) {
                System.out.println(k);
            }

        }

        for (int k = 20; k <= 90 ; k++) {
            if(k%5 ==0) {
                System.out.println(k);
            }

        }
        }
    }
